package com.example.petscheck;

public class Pets {
    public String id, name, age, weight, breed;

    public Pets() {
    }

    public Pets(String id, String name, String age, String weight, String breed) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.weight = weight;
        this.breed = breed;
    }
}
